// Helper function to get formatted price display
export const getFormattedPrice = (item) => {
  const title = item.title.toLowerCase();
  
  // Check if product has specific weight mentioned in title (gms, kg, etc.)
  const hasSpecificWeight = /\d+\s*(gms?|kgs?|g|kg|grams?|kilograms?)/i.test(title);
  
  // Check if product has parentheses (likely indicates unit/pack pricing)
  const hasParentheses = /\([^)]*\)/.test(title);
  
  // Check if product is per piece (pc)
  const isPerPiece = /\(pc\)/i.test(title);
  
  // Check if product is bun, bread, donut type (per unit items)
  const isPerUnitItem = /\b(bun|bread|donut|dilpasand|doughnut|roll|croissant|muffin|bagel|loaf)\b/i.test(title);
  
  if (hasSpecificWeight || hasParentheses || isPerPiece || isPerUnitItem) {
    // For pre-packaged items, per-piece items, or unit items - show just the price
    return `₹${item.price}`;
  } else {
    // For items sold by weight, show price per kg
    return `₹${item.price}/kg`;
  }
};

// Helper function to get price unit for display
export const getPriceUnit = (item) => {
  const title = item.title.toLowerCase();
  
  // Check various conditions for unit pricing
  const hasSpecificWeight = /\d+\s*(gms?|kgs?|g|kg|grams?|kilograms?)/i.test(title);
  const hasParentheses = /\([^)]*\)/.test(title);
  const isPerPiece = /\(pc\)/i.test(title);
  const isPerUnitItem = /\b(bun|bread|donut|doughnut|roll|croissant|muffin|bagel|loaf)\b/i.test(title);
  
  if (isPerPiece) {
    return 'piece';
  } else if (hasSpecificWeight) {
    return 'pack';
  } else if (isPerUnitItem) {
    return 'unit';
  } else if (hasParentheses) {
    return 'item';
  } else {
    return 'kg';
  }
};

// Helper function to search products across all categories
export const searchProducts = (searchTerm) => {
  const results = []
  
  Object.keys(allProducts).forEach(category => {
    Object.keys(allProducts[category]).forEach(id => {
      const product = allProducts[category][id]
      if (product.title.toLowerCase().includes(searchTerm.toLowerCase()) ||
          product.desc.toLowerCase().includes(searchTerm.toLowerCase())) {
        results.push({ ...product, id: parseInt(id) })
      }
    })
  })
  
  return results
}

